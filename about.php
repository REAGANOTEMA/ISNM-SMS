  <script>
    // Campus Life Slider JavaScript
    let currentSlide = 0;
    const slides = document.querySelectorAll('.slide');
    const indicators = document.querySelectorAll('.indicator');
    const totalSlides = slides.length;
    let autoSlideInterval;

    // Initialize slider
    function initSlider() {
      showSlide(0);
      startAutoSlide();
    }

    // Show specific slide
    function showSlide(index) {
      // Hide all slides
      slides.forEach(slide => slide.classList.remove('active'));
      indicators.forEach(indicator => indicator.classList.remove('active'));
      
      // Show current slide
      slides[index].classList.add('active');
      indicators[index].classList.add('active');
      
      currentSlide = index;
    }

    // Change slide (next/previous)
    function changeSlide(direction) {
      currentSlide += direction;
      
      if (currentSlide >= totalSlides) {
        currentSlide = 0;
      } else if (currentSlide < 0) {
        currentSlide = totalSlides - 1;
      }
      
      showSlide(currentSlide);
      resetAutoSlide();
    }

    // Go to specific slide
    function goToSlide(index) {
      showSlide(index);
      resetAutoSlide();
    }

    // Start automatic slide transition
    function startAutoSlide() {
      autoSlideInterval = setInterval(() => {
        changeSlide(1);
      }, 5000); // 5 seconds
    }

    // Reset automatic slide transition
    function resetAutoSlide() {
      clearInterval(autoSlideInterval);
      startAutoSlide();
    }

    // Pause auto-slide on hover
    function pauseAutoSlide() {
      clearInterval(autoSlideInterval);
    }

    // Resume auto-slide on mouse leave
    function resumeAutoSlide() {
      startAutoSlide();
    }

    // Initialize when DOM is loaded
    document.addEventListener('DOMContentLoaded', function() {
      initSlider();
      
      // Add hover events to slider
      const slider = document.querySelector('.campus-slider');
      if (slider) {
        slider.addEventListener('mouseenter', pauseAutoSlide);
        slider.addEventListener('mouseleave', resumeAutoSlide);
      }
      
      // Add keyboard navigation
      document.addEventListener('keydown', function(e) {
        if (e.key === 'ArrowLeft') {
          changeSlide(-1);
        } else if (e.key === 'ArrowRight') {
          changeSlide(1);
        }
      });
      
      // Add touch/swipe support for mobile
      let touchStartX = 0;
      let touchEndX = 0;
      
      const sliderWrapper = document.querySelector('.slider-wrapper');
      if (sliderWrapper) {
        sliderWrapper.addEventListener('touchstart', function(e) {
          touchStartX = e.changedTouches[0].screenX;
        });
        
        sliderWrapper.addEventListener('touchend', function(e) {
          touchEndX = e.changedTouches[0].screenX;
          handleSwipe();
        });
      }
      
      function handleSwipe() {
        const swipeThreshold = 50;
        const diff = touchStartX - touchEndX;
        
        if (Math.abs(diff) > swipeThreshold) {
          if (diff > 0) {
            // Swipe left - next slide
            changeSlide(1);
          } else {
            // Swipe right - previous slide
            changeSlide(-1);
          }
        }
      }
    });
  </script>

